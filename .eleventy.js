// Eleventy (11ty) configuration file.
// This tells 11ty where source files live, where to put the built output,
// and which files to copy through unchanged (like CSS, JS, and images).

module.exports = function (eleventyConfig) {

	// Enable Nunjucks autoescape — all template output is HTML-escaped by default.
	// Without this, a malicious string in images.json or site.json could inject
	// arbitrary HTML into the page. Templates that need raw output (JSON-LD) already
	// use the `| safe` filter to explicitly opt out of escaping.
	eleventyConfig.setNunjucksEnvironmentOptions({
		autoescape: true,
	});

	// Pass-through copy: these folders are copied to _site/ exactly as-is.
	// 11ty would otherwise only process template files (njk, html, md, etc.).
	eleventyConfig.addPassthroughCopy("src/assets");

	// Favicon and apple-touch-icon — must land at the root of _site/, not under assets/.
	// The object form maps src path → output path relative to _site/.
	eleventyConfig.addPassthroughCopy({ "src/favicon.ico": "favicon.ico" });
	eleventyConfig.addPassthroughCopy({ "src/apple-touch-icon.png": "apple-touch-icon.png" });

	// Cloudflare Pages cache header rules — must land at the root of _site/.
	// Files starting with _ are normally ignored by 11ty, so we explicitly
	// copy this one through. Cloudflare Pages reads it at deploy time.
	eleventyConfig.addPassthroughCopy({ "src/_headers": "_headers" });

	// Deep zoom tile pyramids — generated by `vips dzsave` and stored here
	// during development/testing. In production these move to Cloudflare R2
	// and this folder (and the passthrough) can be removed.
	// Only copies if the directory exists — safe to leave here permanently.
	eleventyConfig.addPassthroughCopy("src/tiles");

	// A filter to format a date string like "2025-11-14" into "November 14, 2025".
	// Filters are called in templates with the pipe syntax: {{ image.date | readableDate }}
	//
	// Accepts two forms:
	//   - A string ("2025-11-14") — from images.json dates
	//   - A JavaScript Date object — Eleventy passes these for collection page dates
	//     (e.g. {{ guide.date | readableDate }} on the guides listing page)
	eleventyConfig.addFilter("readableDate", function (dateInput) {
		// If already a Date object, use it directly. Otherwise parse the string at
		// noon UTC to avoid timezone-rollover shifting the date by one day.
		const date = dateInput instanceof Date
			? dateInput
			: new Date(dateInput + "T12:00:00Z");
		return date.toLocaleDateString("en-US", {
			year: "numeric",
			month: "long",
			day: "numeric",
			timeZone: "UTC",
		});
	});

	// Formats exposure time into a human-readable string like "6h 20m".
	// Accepts either:
	//   - a plain number of minutes (legacy scalar form)
	//   - an array of filter objects like [{name, frames, minutes}, ...]
	//     in which case it sums the minutes across all filters.
	// Returns "—" for solar/lucky-imaging entries where minutes are null.
	eleventyConfig.addFilter("formatExposure", function (input) {
		// Sum an array of filter objects
		if (Array.isArray(input)) {
			const total = input.reduce((sum, f) => sum + (f.minutes || 0), 0);
			if (total === 0) return "—";
			input = total; // fall through to the formatting block below
		}
		if (!input) return "—";
		const h = Math.floor(input / 60);
		const m = input % 60;
		if (h === 0) return `${m}m`;
		if (m === 0) return `${h}h`;
		return `${h}h ${m}m`;
	});

	// Sums the frame counts from an acquisition filter array.
	// Accepts an array of filter objects like [{name, frames, minutes}, ...].
	// Returns the total number of frames, or 0 if no data.
	eleventyConfig.addFilter("totalFrames", function (filters) {
		if (!Array.isArray(filters)) return 0;
		return filters.reduce(function (sum, f) { return sum + (f.frames || 0); }, 0);
	});

	// Converts field of view from degrees to a human-readable arcminute string.
	// Accepts width and height in degrees (from sky.fov_w, sky.fov_h).
	// Returns a string like "166' x 111'" or null if either value is missing.
	eleventyConfig.addFilter("fovArcmin", function (fovW, fovH) {
		if (!fovW || !fovH) return null;
		var w = Math.round(fovW * 60);
		var h = Math.round(fovH * 60);
		return w + "' \u00D7 " + h + "'";
	});

	// Maps a Bortle class integer (1-9) to a human-readable label.
	// Used by the light pollution gauge on detail pages.
	var bortleLabels = [
		"", "Excellent dark site", "Typical truly dark site", "Rural sky",
		"Rural/suburban transition", "Suburban sky", "Bright suburban sky",
		"Suburban/urban transition", "City sky", "Inner-city sky"
	];
	eleventyConfig.addFilter("bortleLabel", function (bortle) {
		return bortleLabels[bortle] || "";
	});

	// Splits a string on a given separator and returns an array.
	// Nunjucks doesn't have a built-in split filter.
	// Used by the processing notes section to turn double-newline-separated
	// text into paragraphs: {% for para in notes | split('\n\n') %}
	eleventyConfig.addFilter("split", function (str, sep) {
		return str ? str.split(sep) : [];
	});

	return {
		// Tell 11ty where source files live and where to write built output.
		// input:    all templates and content live under src/
		// output:   built site written to _site/ — what Cloudflare Pages deploys
		// includes: partials and layouts live under src/_includes/
		//           referenced in templates as {% include "partials/header.njk" %}
		// data:     global data files (images.json, site.json, year.js) live under src/_data/
		//           each file becomes a template variable named after the file (without extension)
		dir: {
			input: "src",
			output: "_site",
			includes: "_includes",
			data: "_data",
		},
	};
};
